package edu.handong.database.team6;

public class MusicUser {
	int id;
	String myList;
	int count;
	String voucher;
	
	public String getVoucher() {
		return voucher;
	}
	public void setVoucher(String voucher) {
		this.voucher = voucher;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getMyList() {
		return myList;
	}
	public void setMyList(String myList) {
		this.myList = myList;
	}
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	
}
